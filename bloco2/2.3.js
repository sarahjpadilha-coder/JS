const dia = 3;


switch (dia) {
  case 1:
    console.log("Domingo");
    break;
  case 2:
    console.log("Segunda");
    break;
  case 3:
    console.log("Terça");
    break;
  case 4:
    console.log("quarta");
    break;
  case 5:
    console.log("quinta");
    break;
  case 6:
    console.log("sexta");
    break;
  case 7:
    console.log("sábado");
    break;
  default:
    console.log("Outro dia");
}